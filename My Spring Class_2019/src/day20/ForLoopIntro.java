package day20;

public class ForLoopIntro {

	public static void main(String[] args) {
		

		

		for(int i = 0; i<5; i++) {
			System.out.println("Happy birthday");
		}
		
		
		for(int n = 1; n<11; n++) {
			System.out.print(n+" ");
		}
		
		
		System.out.println("");
		
		for(int n = 10; n>=1; n--) {
			System.out.print(n+" ");
		}
		
	}

}
